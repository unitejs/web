/**
 * Gulp utils for display.
 */
const colors = require("ansi-colors");
const fancyLog = require("fancy-log");

function log(text) {
    fancyLog(text);
}
exports.log = log;

function info(caption, args) {
    if (args === undefined) {
        fancyLog(`[${colors.cyan(caption)}]`);
    } else if (Array.isArray(args)) {
        args.forEach(arg => {
            fancyLog(`[${colors.cyan(caption)}]`, arg);
        });
    } else {
        fancyLog(`[${colors.cyan(caption)}]`, args);
    }
}
exports.info = info;

function error(text, err) {
    fancyLog(colors.red(`[ERROR] ${text}`));
    if (err !== undefined) {
        if (err instanceof Error) {
            fancyLog(colors.red(`${err.message}`));
            if (err.stack) {
                fancyLog(colors.red(`${err.stack}`));
            }
        } else {
            fancyLog(colors.red(`${err}`));
        }
    }
}
exports.error = error;

function warning(text) {
    fancyLog(colors.yellow(`[WARNING] ${text}`));
}
exports.warning = warning;

function success(text) {
    fancyLog(colors.green(text));
}
exports.success = success;
